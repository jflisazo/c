#include<stdio.h>
#include<stdlib.h>
#include<ctype.h>

#define MAXOP	100
#define NUMBER	'0'
#define MAXVAL	100
#define BUFSIZE	100

/*Declaraci√≥n de funciones prototipo*/
int getop(char []);
void push(double);
double pop(void);
char buf[BUFSIZE];
int bufp = 0;
int getch(void);
